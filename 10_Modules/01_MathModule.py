# Modules is a way to reuse code in Python.
import math

math.sqrt(16)    # This will return 4.0, the square root of 16.
print(math.pow(2,5))

dir(math)  # This will list all the attributes and methods of the math module.

math.pi  # This will return the value of pi, approximately 3.14159.
math.log10(100)  # This will return the base-10 logarithm of 100, which is 2.0.
math.floor(2.3) # This will return the largest integer less than or equal to 2.3, which is 2.
math.ceil(2.3)  # This will return the smallest integer greater than or equal to 2.3, which is 3.
math.factorial(5)  # This will return the factorial of 5, which is 120.
math.gcd(12, 15)  # This will return the greatest common divisor of 12 and 15, which is 3.
