#  Lambda Function
# Create a lambda function to compute the cube of a number.


cube = lambda x: x ** 3  # Takes one input: x, Returns: x ** 3 → the cube of x  
                         #  its equivalent to  def cube(x):  return x ** 3
   
print(cube(3))



# A lambda is a small anonymous function in Python.
# It’s often used for short, one-line functions.
# Syntax: lambda arguments: expression

